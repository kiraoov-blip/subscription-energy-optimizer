def _load_input_file(uploaded_file) -> Tuple[str, tempfile.TemporaryDirectory | None]:
    temp_dir = tempfile.TemporaryDirectory()
    input_path = Path(temp_dir.name) / "input.xlsx"

    if uploaded_file is None:
        # 입력 Excel을 코드 안에 내장하여 GitHub 파일명·경로 문제를 원천 차단합니다.
        encoded = "".join(EMBEDDED_DEFAULT_XLSX_B64.split())
        input_path.write_bytes(base64.b64decode(encoded))
    else:
        input_path.write_bytes(uploaded_file.getvalue())

    return str(input_path), temp_dir

